/*
====================================
📄 Dart 筆記：函式、方法、函數 區分
====================================

🔹 函式 (Function)
- 程式裡獨立可呼叫的程式區塊
- 不屬於任何類別 (class)
- 範例：
  int add(int a, int b) {
    return a + b;
  }

🔹 方法 (Method)
- 定義在類別 (class) 裡面的函式
- 與物件（object）狀態、屬性有關
- 範例：
  class Person {
    void sayHello() {
      print("Hello!");
    }
  }

🔹 函數
- 數學用語，表示輸入對應輸出的規則
- 範例：f(x) = x² + 1
- 程式裡通常不會用「函數」稱呼 function

------------------------------------

🧠 小口訣
函式 = function（程式裡獨立用）
方法 = method（class 裡面）
函數 = 數學 function（程式幾乎不用）

------------------------------------

💡 Dart 沒有「function」或「函數」關鍵字
宣告函式都用回傳型別（如 int、void）＋函式名稱

====================================
*/
